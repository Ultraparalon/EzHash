static void	command_mode(void)
{
	char **argv;
	char *tmp;

	tmp = NULL;
	ft_printf("Type 'q' to quit\n");
	while (1)
	{
		ft_printf("ft_ssl> ");
		if (get_next_line(0, &tmp) < 1 || !ft_strcmp(tmp, "q"))
			break ;
		argv = ft_strsplit(tmp, ' ');
		wizzard(argv);
		ft_strdel(&tmp);
		ft_card_destructor((void **)argv);
	}
	ft_strdel(&tmp);
}

int			main(int argc, char **argv)
{
	if (argc < 2)
	{
		command_mode();
	}
	else
	{
		wizzard(++argv);
	}
	return (0);
}
